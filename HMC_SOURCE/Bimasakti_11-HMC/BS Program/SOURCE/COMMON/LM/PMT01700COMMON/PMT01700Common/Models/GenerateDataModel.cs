        private static R ConvertDigitalSignReportFromDataDBtoDataReport<F, R>(F poParameter) where R : new()
        {
            if (poParameter == null) return new R();

            R loResult = new R();
            Type typeF = typeof(F);
            Type typeR = typeof(R);

            for (int i = 1; i <= 6; i++)
            {
                string storagePropertyName = $"CSIGN_STORAGE_ID0{i}";
                string imagePropertyName = $"OSIGN_IMAGE0{i}";

                string[] propertyNames =
                {
                    //$"CSIGN_ID0{i}",
                    $"CSIGN_NAME0{i}", $"CSIGN_POSITION0{i}", storagePropertyName
                };
                foreach (string propertyName in propertyNames)
                {
                    var sourceProperty = typeF.GetProperty(propertyName);
                    var targetProperty = typeR.GetProperty(propertyName);

                    if (sourceProperty == null) continue; // Skip jika properti tidak ditemukan

                    object? value = sourceProperty.GetValue(poParameter);

                    // Jika bukan CSIGN_STORAGE_ID0{i}, set nilai langsung ke target
                    if (propertyName != storagePropertyName)
                    {
                        if (targetProperty != null && targetProperty.CanWrite)
                        {
                            targetProperty.SetValue(loResult, value);
                        }
                    }
                    else
                    {
                        // Jika CSIGN_STORAGE_ID0{i}, gunakan untuk generate OSIGN_IMAGE0{i}
                        var imageProperty = typeR.GetProperty(imagePropertyName);
                        if (imageProperty != null && imageProperty.CanWrite && value is string storageId && !string.IsNullOrEmpty(storageId))
                        {
                            var imageData = GenerateDataLogo(storageId);
                            imageProperty.SetValue(loResult, imageData);
                        }
                        else
                        {
                            imageProperty.SetValue(loResult, null);
                        }
                    }
                }
            }

            return loResult;
        }

